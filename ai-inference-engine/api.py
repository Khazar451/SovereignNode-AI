# ---------------------------------------------------------------------------
# Pydantic schemas
# ---------------------------------------------------------------------------

class RawTelemetry(BaseModel):
    temperature: float = Field(..., ge=-50, le=1200)
    vibration:   float = Field(..., ge=0, le=1000)
    timestamp:   str   = Field(...)


class InsightRequest(BaseModel):
    sensor_id:    str          = Field(..., min_length=1, max_length=64)
    anomaly_type: str          = Field(...)
    raw_telemetry: RawTelemetry = Field(...)
    query:        str          = Field(..., min_length=10, max_length=2000)

    @field_validator("sensor_id")
    @classmethod
    def sensor_id_alphanumeric(cls, v: str) -> str:
        import re
        if not re.match(r"^[A-Za-z0-9_\-]+$", v):
            raise ValueError("sensor_id must only contain alphanumerics, hyphens, underscores")
        return v


class InsightResponse(BaseModel):
    insight:             str         = Field(...)
    confidence_score:    float       = Field(..., ge=0.0, le=1.0)
    sources_referenced:  List[str]   = Field(...)
    inference_time_ms:   float       = Field(...)


class ChatMessage(BaseModel):
    role:    str = Field(..., pattern="^(user|assistant|system)$")
    content: str = Field(..., min_length=1)


class ChatRequest(BaseModel):
    """Free-form chat request, optionally grounded in the RAG knowledge base."""
    messages:    List[ChatMessage] = Field(..., min_length=1)
    use_rag:     bool              = Field(default=True,  description="Augment with RAG context")
    stream:      bool              = Field(default=False, description="Stream response tokens")
    top_k:       int               = Field(default=cfg.TOP_K_RESULTS, ge=1, le=10)
    temperature: float             = Field(default=cfg.TEMPERATURE, ge=0.0, le=2.0)


class ChatResponse(BaseModel):
    answer:           str       = Field(...)
    confidence_score: float     = Field(..., ge=0.0, le=1.0)
    sources:          List[str] = Field(...)
    inference_time_ms: float    = Field(...)
    rag_chunks_used:  int       = Field(...)


# ---------------------------------------------------------------------------
# Ollama helper
# ---------------------------------------------------------------------------

def _ollama_chat(
    messages: list[dict],
    stream: bool = False,
    temperature: float = cfg.TEMPERATURE,
) -> str | requests.Response:
    """
    Call the Ollama /api/chat endpoint.
    Returns either a complete string (stream=False) or a streaming Response.
    """
    payload = {
        "model": OLLAMA_MODEL,
        "messages": messages,
        "stream": stream,
        "options": {
            "temperature": temperature,
            "top_p": cfg.TOP_P,
            "repeat_penalty": cfg.REPETITION_PENALTY,
            "num_predict": cfg.MAX_NEW_TOKENS,
        },
    }

    if stream:
        response = requests.post(
            f"{OLLAMA_BASE_URL}/api/chat",
            json=payload,
            stream=True,
            timeout=300,
        )
        response.raise_for_status()
        return response  # caller handles streaming

    response = requests.post(
        f"{OLLAMA_BASE_URL}/api/chat",
        json=payload,
        timeout=300,
    )
    response.raise_for_status()
    data = response.json()
    return data["message"]["content"].strip()


# ---------------------------------------------------------------------------
# Confidence scoring
# ---------------------------------------------------------------------------

def _compute_confidence(result: RAGResult) -> float:
    """
    Return the average cross-encoder confidence from the RAGResult.
    Falls back to cosine similarity if reranker was not used.
    """
    if result.avg_confidence > 0:
        return round(min(1.0, max(0.0, result.avg_confidence)), 4)
    # Fallback: average cosine similarity
    if not result.retrieved_chunks:
        return 0.0
    avg = sum(c.similarity_score for c in result.retrieved_chunks) / len(result.retrieved_chunks)
    return round(min(1.0, max(0.0, avg)), 4)


def _compute_confidence_from_chunks(chunks) -> float:
    """Compute confidence from retrieved chunks (uses cross-encoder score if available)."""
    if not chunks:
        return 0.0
    avg = sum(c.confidence for c in chunks) / len(chunks)
    return round(min(1.0, max(0.0, avg)), 4)


